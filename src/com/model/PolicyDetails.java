package com.model;

public class PolicyDetails {

	private long id;
	private long cid;
	private long paamt;
	private long pid;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getCid() {
		return cid;
	}

	public void setCid(long cid) {
		this.cid = cid;
	}

	public long getPaamt() {
		return paamt;
	}

	public void setPaamt(long paamt) {
		this.paamt = paamt;
	}

	public long getPid() {
		return pid;
	}

	public void setPid(long pid) {
		this.pid = pid;
	}

}
